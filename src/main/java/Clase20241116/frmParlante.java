    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        
        if (listaParlantes.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No hay parlantes registrados.");
        return;
        }

        for (Parlante parlante : listaParlantes) {
            System.out.println("Sonido: " + parlante.getSonido() + ", Rendimiento: " + parlante.getRendimiento());
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

