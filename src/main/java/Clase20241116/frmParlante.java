    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        
        String sonido = txtSonido.getText();
        String rendimiento = txtRendimiento.getText();

        // Crear un nuevo objeto Parlante y agregarlo a la lista
        Parlante nuevoParlante = new Parlante(sonido, rendimiento);
        listaParlantes.add(nuevoParlante);

        // Limpiar los campos después de agregar
        txtSonido.setText("");
        txtRendimiento.setText("");

        // Mostrar mensaje de éxito
        JOptionPane.showMessageDialog(this, "Parlante registrado correctamente!");
        
    }//GEN-LAST:event_btnAceptarActionPerformed

